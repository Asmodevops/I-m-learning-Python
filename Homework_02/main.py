# задача 10
# Дано число, которое состоит из 4 цифр(цельное). 
# создать новое число из этих же цифр, но расположенных в порядке увеличения
# пример
# вход: 2341
# выход : 1234

count = int(input('\nВведите любое число. Программа расположит цифры в этом числе в порядке возростания.\n'))

array = []

while count > 0:
    array.append(count % 10)
    count = count // 10

array.sort()

result = ''

for i in array:
    result += str(i)


print(int(result))