# Пользователь вводит желаемую фигуру (“круг”, “квадрат”, “прямоугольник”, “треугольник”)
# Нарисовать данную фигуру при помощи модуля turtle
# Задача 5       (я объединил их)
# Пользователь выбирает хочет ли он закрасить круг или нет. 
# В зависимости от выбора пользователя нарисовать при помощи модуля turtle закрашенный круг или полый круг 

from turtle import *
from time import *

figure = input('\nКакую фигуру вам нарисовать (круг, квадрат, треугольник, прямоугольник)?').lower()

match figure:
    case 'круг':
        fillcolor = input('\nХотите закрасить круг (да/нет)?\n').lower()
        match fillcolor:
            case 'да':
                begin_fill()
                color('blue')
                circle(radius=90)
                end_fill()
                sleep(5)
            case 'нет':
                circle(radius=90)
                sleep(5)

    case 'квадрат':
        forward(200)
        left(90)
        forward(200)
        left(90)
        forward(200)
        left(90)
        forward(200)
        sleep(5)

    case 'треугольник':
        forward(200)
        left(120)
        forward(200)
        left(120)
        forward(200)
        left(120)
        forward(200)
        sleep(5)

    case 'прямоугольник':
        forward(200)
        left(90)
        forward(100)
        left(90)
        forward(200)
        left(90)
        forward(100)
        sleep(5)

    case _:
        print('Некорретный вод.')

