# Дана функция по вычислению четности числа.
# Определите верно ли она работает, если есть проблемы опишите где и в чем проблема и исправьте

# def is_even(num):
#     return num // 2 == 0

# Функция работает не верно, так как оператор // означает целочисленное деление.
# Четность числа проверяется посредствам деления по модулю, с помощью оператора %.
# В таком случае функция проверит остаток от деления. Если при делелии на 2, он будет равным 0,
# то число переданное в num будет четным.

# Правильная функция:

def is_even(num):
    return num % 2 == 0

print(is_even(45))
input('Чтобы продолжить, нажмите Enter...')


